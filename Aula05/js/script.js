// No JS não separa os tipos de numero, é tudo number

var num = 5;
console.log(typeof num);

var float = 10.2;
console.log(typeof float);

var text = "10";
console.log(typeof text);

// Os elementos abaixo também são Number
console.log(typeof NaN);
console.log(typeof +Infinity);
console.log(typeof -Infinity);